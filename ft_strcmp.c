#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	result;

	result = 0;
	while((*s1 != '\0' && *s2 != '\0') && ((*s1 - *s2) == 0))
	{
		s1++;
		s2++;
	}
	if ((*s1 - *s2) < 0)
		result = -1;
	else if ((*s1 - *s2) > 0)
		result = 1;
	else
		result = 0;
	return (result);
}
