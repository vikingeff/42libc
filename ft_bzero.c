#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	loop;
	char	*str;

	str = s;
	if (n != 0)
	{
		loop = 0;
		while (loop < n)
		{
			*(str + loop) = 0;
			loop++;
		}
	}
}
